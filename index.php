    $(".faq_section .faq_box > a").on("click", function () {
        if ($(this).hasClass("active")) {
          $(this).removeClass("active");
          $(this)
            .siblings(".content")
            .slideUp(200);
          $(".faq_box > a i")
            .removeClass("fa-minus")
            .addClass("fa-plus");
